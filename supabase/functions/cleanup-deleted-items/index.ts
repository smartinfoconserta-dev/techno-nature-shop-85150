import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.80.0'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

Deno.serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    console.log('üóëÔ∏è Iniciando limpeza de itens deletados h√° mais de 40 dias...');

    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? '',
      {
        auth: {
          autoRefreshToken: false,
          persistSession: false
        }
      }
    );

    // Executar fun√ß√£o SQL de limpeza
    const { data, error } = await supabaseClient.rpc('cleanup_old_deleted_items');

    if (error) {
      console.error('‚ùå Erro ao executar limpeza:', error);
      throw error;
    }

    console.log('‚úÖ Limpeza conclu√≠da com sucesso');

    return new Response(
      JSON.stringify({
        success: true,
        message: 'Limpeza de itens deletados conclu√≠da com sucesso',
        timestamp: new Date().toISOString(),
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      }
    );
  } catch (error) {
    console.error('‚ùå Erro na limpeza:', error);
    return new Response(
      JSON.stringify({
        success: false,
        error: error instanceof Error ? error.message : 'Erro desconhecido',
      }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 500,
      }
    );
  }
});
